package com.pizzaapplication.exception;

public class SuccessMessage {
	private Integer successCode;
	private String successCodeDescription;
	private String successMessage;
	public Integer getSuccessCode() {
		return successCode;
	}
	public void setSuccessCode(Integer successCode) {
		this.successCode = successCode;
	}
	public String getSuccessCodeDescription() {
		return successCodeDescription;
	}
	public void setSuccessCodeDescription(String successCodeDescription) {
		this.successCodeDescription = successCodeDescription;
	}
	public String getSuccessMessage() {
		return successMessage;
	}
	public void setSuccessMessage(String successMessage) {
		this.successMessage = successMessage;
	}
	

}
